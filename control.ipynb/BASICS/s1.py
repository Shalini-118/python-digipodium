# Defining strings in python
# all of the following are equivalent
my_string='hello'
print(my_string)

my_string="hello"
print(my_string)

my_string='''hello'''
print(my_string)

#triple quotes string can extend multiole lines
my_string="""hello"""
print(my_string)
print(my_string[-1])
print(my_string[-2])
print(my_string[-3])
print(my_string[-4])
print(my_string[1])
print(my_string[-2])
print(my_string[-10])